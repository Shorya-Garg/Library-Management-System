package EntityPackage;

public class Book {
	
	public String name;
	public String author;
	public String bookId;
	public String library;
	public int price;
	public double rating;
	public Book(String name, String author, String library,String bookId, int price, double rating) {
		this.name = name;
		this.author = author;
		this.bookId = bookId;
		this.library = library;
		this.price = price;
		this.rating = rating;
	}
}

 


 
